export const totalLessonsCount = curriculum.reduce(
  (sum, level) => sum + level.lessons.length,
  0
);

export function getLevel(levelId) {
  return curriculum.find((l) => l.id === levelId);
}

export function getLesson(levelId, lessonId) {
  const level = getLevel(levelId);
  if (!level) return null;
  const idx = level.lessons.findIndex((les) => les.id === lessonId);
  if (idx === -1) return null;
  return {
    level,
    lesson: level.lessons[idx],
    prevLesson: level.lessons[idx - 1] || null,
    nextLesson: level.lessons[idx + 1] || null,
  };
}
